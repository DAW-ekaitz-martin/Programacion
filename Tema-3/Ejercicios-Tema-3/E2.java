public class E2 {
    public static void main(String[] args) {
        System.out.println("BUCLE FOR");
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
        System.out.println("BUCLE WHILE");
        int i = 10;
        while (i >= 1) {
            System.out.println(i);
            i--;
        }
    }
}
